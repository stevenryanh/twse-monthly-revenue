def pools_of(code):
    """回傳某代碼所屬的 ETF 池標籤清單；ETF 代碼本身回 ['ETF']。"""
    if code in ETF_LABELS:
        return ["ETF"]
    return [label for etf, label in ETF_LABELS.items() if code in ETF_CONSTITUENTS[etf]]


def constituents_union():
    """四檔成分股的聯集（去重、保序）。"""
    seen, out = set(), []
    for lst in ETF_CONSTITUENTS.values():
        for c in lst:
            if c not in seen:
                seen.add(c)
                out.append(c)
    return out
