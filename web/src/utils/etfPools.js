// ETF 代碼 → (短標籤, 成分股集合)
const POOLS = [
  { code: '0050', label: '權', set: new Set(TW50) },
  { code: '0056', label: '息', set: new Set(HIGH_DIV_0056) },
  { code: '00878', label: '永', set: new Set(ESG_00878) },
  { code: '00713', label: '波', set: new Set(LOWVOL_00713) },
]
const ETF_CODES = new Set(POOLS.map((p) => p.code))

/** 回傳某代碼所屬池的短標籤（如「權·息·波」）；ETF 代碼本身回「ETF」；無則回「—」。 */
export function poolsShort(code) {
  if (ETF_CODES.has(code)) return 'ETF'
  const tags = POOLS.filter((p) => p.set.has(code)).map((p) => p.label)
  return tags.length ? tags.join('·') : '—'
}
